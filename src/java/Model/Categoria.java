package Model;

public class Categoria {

    private int id;

    private String nome;

    public Categoria(int id) {
        this.id = id;
    }

    public Categoria() {

    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

}
